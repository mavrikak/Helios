HC_EV_NM = 1239.841984
C_M_S = 299792458.0
TWOPI = 2*np.pi

def read_csc_values(csc_file: Path) -> Tuple[float, float, float]:
    """Parse first non-comment line of a .csc file into (SCS, ACS, EXT)."""
    with open(csc_file, "r") as f:
        for line in f:
            s = line.strip()
            if not s or s[0] in "#%/": continue
            s = s.replace(",", " ").replace(";", " ")
            vals: List[float] = []
            for tok in s.split():
                try: vals.append(float(tok))
                except Exception: pass
            if len(vals) >= 3: return vals[0], vals[1], vals[2]
    raise ValueError(f"Could not parse values from {csc_file}")

def _extract_number_unit_pairs(text: str) -> List[Tuple[float, str]]:
    """Utility for extracting (value, unit) pairs from text [nm, µm, m, eV, Hz..]."""
    pairs: List[Tuple[float, str]] = []
    for m in re.finditer(r"([-+]?\d*\.?\d+(?:[eE][-+]?\d+)?)\s*(nm|um|µm|micron|microns|m|hz|khz|mhz|ghz|thz|ev)\b", text, re.IGNORECASE):
        val = float(m.group(1)); unit = m.group(2).lower(); pairs.append((val, unit))
    return pairs

def _to_nm_from_value_unit(val: float, unit: str) -> Optional[float]:
    """Convert a (value, unit) pair to wavelength in nm (returns None if unknown)."""
    unit = unit.lower()
    if unit == "nm": return val
    if unit in ("um","µm","micron","microns"): return val*1e3
    if unit == "m": return val*1e9
    if unit == "ev": return HC_EV_NM/val if val!=0 else None
    if unit in ("hz","khz","mhz","ghz","thz"):
        mul = {"hz":1.0,"khz":1e3,"mhz":1e6,"ghz":1e9,"thz":1e12}[unit]
        f = val*mul
        return (C_M_S/f)*1e9 if f!=0 else None
    return None

def _parse_lambda_nm_from_xml(xml_path: Path) -> Optional[float]:
    """
    Heuristically extract wavelength [nm] from a job XML by checking tags, attributes,
    and free text; also accepts frequency (ω or f) with unit conversion.
    """
    txt = xml_path.read_text(errors="ignore")
    m = re.search(r"<\s*(wavelength|lambda|vacuum_wavelength|lambda0)\b[^>]*unit\s*=\s*\"([^\"]+)\"[^>]*>([^<]+)<", txt, re.IGNORECASE)
    if m:
        unit = m.group(2).strip().lower()
        nums = re.findall(r"[-+]?\d*\.?\d+(?:[eE][-+]?\d+)?", m.group(3))
        if nums:
            val = float(nums[0]); lam = _to_nm_from_value_unit(val, unit)
            if lam is not None: return lam
    m = re.search(r"<\s*(wavelength|lambda|vacuum_wavelength|lambda0)\b[^>]*>([^<]+)<", txt, re.IGNORECASE)
    if m:
        blob = m.group(2); pairs = _extract_number_unit_pairs(blob)
        if pairs:
            lam = _to_nm_from_value_unit(*pairs[0]); 
            if lam is not None: return lam
        nums = re.findall(r"[-+]?\d*\.?\d+(?:[eE][-+]?\d+)?", blob)
        if nums: return float(nums[0])
    m = re.search(r"<\s*(frequency|freq|f|omega|w)\b[^>]*unit\s*=\s*\"([^\"]+)\"[^>]*>([^<]+)<", txt, re.IGNORECASE)
    if m:
        unit = m.group(2).strip().lower(); nums = re.findall(r"[-+]?\d*\.?\d+(?:[eE][-+]?\d+)?", m.group(3))
        if nums:
            val = float(nums[0])
            if m.group(1).lower() in ("omega","w"): val = val/TWOPI; unit="hz"
            lam = _to_nm_from_value_unit(val, unit)
            if lam is not None: return lam
    for key in ("wavelength","lambda","vacuum_wavelength","lambda0","frequency","freq","f","omega","w"):
        m = re.search(rf"{key}\s*=\s*\"([^\"]+)\"", txt, re.IGNORECASE)
        if m:
            for val, unit in _extract_number_unit_pairs(m.group(1)):
                lam = _to_nm_from_value_unit(val, unit)
                if lam is not None: return lam
    for val, unit in _extract_number_unit_pairs(txt):
        lam = _to_nm_from_value_unit(val, unit)
        if lam is not None: return lam
    return None

def ensure_lambdalist(sim: str, root: Path, lambda_file: Optional[Path] = None) -> Dict[str, float]:
    """
    Ensure sim/lambdalist.txt exists; if absent, derive λ per job.N.xml and write it.
    Returns dict label -> λ_nm.
    """
    simdir = sim_root_dir(sim, root)
    lf = lambda_file if lambda_file else (simdir / "lambdalist.txt")
    if lf.exists():
        mapping: Dict[str,float] = {}
        with open(lf,"r") as f:
            for line in f:
                s=line.strip()
                if not s or s[0] in "#%/": continue
                parts=s.split()
                if len(parts)>=2:
                    try: mapping[parts[0]]=float(parts[1])
                    except: pass
        if mapping: return mapping
    jobs_dir = sim_jobs_dir(sim, root)
    if not jobs_dir.exists(): raise FileNotFoundError(f"Jobs dir not found: {jobs_dir}")
    xmls = sorted(jobs_dir.glob("*.xml"))
    if not xmls: raise FileNotFoundError(f"No job XMLs in {jobs_dir}")
    mapping: Dict[str,float] = {}
    for x in xmls:
        lam_nm = _parse_lambda_nm_from_xml(x)
        if lam_nm is not None: mapping[x.stem]=lam_nm
    if not mapping:
        placeholder = simdir / "lambdalist.txt"
        with open(placeholder,"w") as f:
            f.write("# Could not auto-extract wavelengths; fill as: <label> <wavelength_nm>\n")
            for x in xmls: f.write(f"{x.stem} 0\n")
        raise RuntimeError(f"Could not extract wavelengths. Edit {placeholder} and rerun.")
    with open(lf,"w") as f:
        f.write("# label  wavelength_nm\n")
        for k,v in mapping.items(): f.write(f"{k} {v:.9g}\n")
    print(f"[info] wrote {lf}")
    return mapping

def _best_label_match_by_index(stem: str, labels: List[str]) -> Optional[str]:
    """
    Prefer numeric index mapping: sim1_* -> job.1, job2_* -> job.2, *_<n> -> job.<n>, etc.
    """
    # extract first integer index
    m = re.search(r"(?:^|[^\d])(\d+)(?:[^\d]|$)", stem)
    if m:
        idx = m.group(1)
        # try exact 'job.idx'
        cand = f"job.{idx}"
        if cand in labels: return cand
        # try any label ending with idx or having .idx
        for L in labels:
            if L.endswith(f".{idx}") or re.search(rf"(?:^|[^\d]){idx}(?:[^\d]|$)", L):
                return L
    # substring heuristic fallback
    for L in labels:
        if L in stem or stem in L: return L
    return None

def _dedupe_or_fallback(lams: np.ndarray, stems: List[str], labels: List[str], lambda_map: Dict[str,float]) -> np.ndarray:
    """If duplicate λ's dominate but lengths match, align by sorted order as a fallback."""
    if len(np.unique(lams)) < len(lams) and len(labels) == len(lams):
        sorted_stems = sorted(stems)
        sorted_labels = sorted(labels)
        remap = {st: la for st, la in zip(sorted_stems, sorted_labels)}
        lam2 = np.array([lambda_map.get(remap.get(st, ""), np.nan) for st in stems], dtype=float)
        return lam2
    return lams


# ------------------------- material helpers (Mie) -------------------------
def _parse_scalar_spec(spec: str):
    s = spec.strip().lower()
    if s.startswith("eps="):
        a,b = s.split("=",1)[1].replace(" ","").split(",")
        eps = complex(float(a), float(b))
        n = np.sqrt(eps+0j)
        return lambda lam_nm: np.full_like(lam_nm, n, dtype=np.complex128)
    if s.startswith("nk="):
        a,b = s.split("=",1)[1].replace(" ","").split(",")
        n = complex(float(a), float(b))  # n + i k
        return lambda lam_nm: np.full_like(lam_nm, n, dtype=np.complex128)
    return None

def _try_material_file(path: Path):
    if path.exists() and path.is_file():
        return path
    for ext in (".txt",".dat",".csv"):
        p = path.with_suffix(ext)
        if p.exists():
            return p
    return None

def _load_material_table(fp: Path):
    """Load table with columns:
       (λ[nm], n, k)  or  (λ[nm], Re(ε), Im(ε))  or  constant: (n, k) / (Re(ε), Im(ε)).
       Returns (lam_nm or None, n_complex or eps_complex)."""
    rows = []
    with open(fp, "r", encoding="utf-8", errors="ignore") as f:
        for line in f:
            s = line.strip()
            if not s or s[0] in "#%;/":
                continue
            toks = [t for t in re.split(r"[,\s;]+", s) if t]
            try:
                vals = list(map(float, toks))
            except Exception:
                continue
            if len(vals) >= 2:
                rows.append(vals)
    if not rows:
        raise RuntimeError(f"No numeric rows parsed from {fp}")
    arr = np.asarray(rows, dtype=float)
    if arr.shape[1] == 2:
        # constant across λ
        v1, v2 = arr[0,0], arr[0,1]
        return None, (v1 + 1j*v2, None)  # ambiguous; caller decides nk vs. eps
    # assume first column is λ[nm]
    lam = arr[:,0]
    v1, v2 = arr[:,1], arr[:,2]
    return lam, (v1 + 1j*v2, None)  # ambiguous; caller decides nk vs. eps

def _material_n_vs_lambda(spec: str, matroot: str, lam_nm: np.ndarray,
                          use_spline: bool = False) -> np.ndarray:
    """Return complex refractive index n(λ) for λ[nm]."""
    # 1) direct scalar spec
    f = _parse_scalar_spec(spec)
    if f:
        return f(lam_nm)
    # 2) file / bare name
    s = spec.strip()
    root = Path(matroot)
    if s.startswith("@"):
        fp = _try_material_file(Path(s[1:]))
        if not fp:
            fp = _try_material_file((root / s[1:]))
    else:
        fp = _try_material_file(root / s)
    if not fp:
        raise FileNotFoundError(f"Material not found: {spec} (searched under {matroot})")
    lam_tab, pair = _load_material_table(fp)
    # Heuristic: if file name contains 'nk', treat as (n,k); if contains 'eps', treat as ε
    is_nk = ("nk" in fp.stem.lower())
    if lam_tab is None:
        # constant across λ
        val = pair[0]
        if is_nk:
            n_const = val
        else:
            n_const = np.sqrt(val+0j)
        return np.full_like(lam_nm, n_const, dtype=np.complex128)
    else:
        v = pair[0]  # complex series (either n+ik or complex eps)
        if is_nk:
            n_ser = v
        else:
            n_ser = np.sqrt(v+0j)
        # interpolate real and imaginary parts on λ-grid
        if use_spline:
            try:
                from scipy.interpolate import interp1d  # optional dependency
                fr = interp1d(lam_tab, np.real(n_ser), kind="cubic",
                              bounds_error=False, fill_value="extrapolate")
                fi = interp1d(lam_tab, np.imag(n_ser), kind="cubic",
                              bounds_error=False, fill_value="extrapolate")
                nr = fr(lam_nm)
                ni = fi(lam_nm)
            except Exception:
                # graceful fallback to linear interpolation
                nr = np.interp(lam_nm, lam_tab, np.real(n_ser))
                ni = np.interp(lam_nm, lam_tab, np.imag(n_ser))
        else:
            nr = np.interp(lam_nm, lam_tab, np.real(n_ser))
            ni = np.interp(lam_nm, lam_tab, np.imag(n_ser))
        return nr + 1j*ni

def plot_csc_line_with_lambda(out_dir: Path,
                              lambda_map: Dict[str, float],
                              series: List[str],
                              save: Optional[Path],
                              show: bool,
                              mie_opts: Optional[dict] = None,
                              subjob: Optional[int] = None):
    """Plot selected cross-section series vs. wavelength after labeling each .csc by λ."""
    files = list_files_with_ext(out_dir, "csc")
    if not files:
        raise FileNotFoundError(f"No .csc files in {out_dir}")

    if subjob is not None:
        filt = [f for f in files if f"inc_{subjob}" in f.stem]
        # Only enforce the filter if we actually found matches
        if filt:
            files = filt

    labels = list(lambda_map.keys())
    stems: List[str] = []
    lam_list: List[float] = []
    sca_list: List[float] = []
    abs_list: List[float] = []
    ext_list: List[float] = []

    for f in files:
        s, a, e = read_csc_values(f)
        lab = _best_label_match_by_index(f.stem, labels)
        lam = lambda_map.get(lab, np.nan)
        stems.append(f.stem); lam_list.append(lam); sca_list.append(s); abs_list.append(a); ext_list.append(e)

    lam = np.array(lam_list, dtype=float)
    lam = _dedupe_or_fallback(lam, stems, labels, lambda_map)

    mask = ~np.isnan(lam)
    lam = lam[mask]; sca = np.array(sca_list)[mask]; abs_ = np.array(abs_list)[mask]; ext = np.array(ext_list)[mask]

    order = np.argsort(lam)
    lam, sca, abs_, ext = lam[order], sca[order], abs_[order], ext[order]

    fig, ax = plt.subplots(figsize=(8.5, 4.8))
    sel_map: Dict[str, Tuple[np.ndarray, str]] = {
        "scs": (sca,  "SIE scs"),
        "acs": (abs_, "SIE acs"),
        "ext": (ext,  "SIE ext"),
    }

    # normalize selection once (used by both simulation & Mie)
    series_set = {s.lower() for s in series} if series else {"scs", "acs", "ext"}
    
    # Standard CSC curves (only selected)
    for key in sorted(series_set):
        y, label = sel_map[key.lower()]
        ax.plot(lam, y, marker="o", label=label)

    if mie_opts and _HAS_MIEPY:
        R = float(mie_opts["radius_nm"])    # radius in nm
        mat = mie_opts["mat"]
        bg = mie_opts.get("bg", "eps=1,0")
        root = mie_opts["matroot"]
        use_spline = bool(mie_opts.get("spline", False))

        # Material refractive indices
        n_part = _material_n_vs_lambda(mat, root, lam, use_spline=use_spline)
        n_bg   = _material_n_vs_lambda(bg,  root, lam, use_spline=use_spline)

        # Relative refractive index and size parameter
        m = n_part / n_bg
        x = 2.0 * np.pi * R * np.abs(n_bg) / lam   # dimensionless

        geo = np.pi * R**2                         # nm^2
        Csca, Cext = [], []

        for mi, xi, lami, nbi in zip(m, x, lam, n_bg):
            try:
                Qext, Qsca, *_ = mie.efficiencies_mx(mi, xi)
            except Exception:
                Qsca = Qext = np.nan

            Csca.append(Qsca * geo)
            Cext.append(Qext * geo)

        Csca = np.array(Csca)
        Cext = np.array(Cext)
        Cabs = Cext - Csca
        if "acs" in series_set:
            ax.plot(lam, Cabs, "--", label=f"Mie acs")
        if "ext" in series_set:
            ax.plot(lam, Cext, "--", label=f"Mie ext")
        if "scs" in series_set:
            ax.plot(lam, Csca, "--", label=f"Mie scs")

    elif mie_opts and not _HAS_MIEPY:
        print("[warn] miepython not installed; skipping Mie overlay.")   

    ax.set_xlabel("Wavelength [nm]")
    ax.set_ylabel(r"Cross section [$\mathrm{nm^2}$]")

    if len(series) == 1:
        key = series[0].lower()
        if key == "scs":
            title = "Scattering Cross Section vs. Wavelength"
        elif key == "acs":
            title = "Absorption Cross Section vs. Wavelength"
        elif key == "ext":
            title = "Extinction Cross Section vs. Wavelength"
        else:
            title = "Cross Section vs. Wavelength"
    else:
        title = "Cross Sections vs. Wavelength"
    ax.set_title(title)

    ax.legend()
    fig.tight_layout()
    if save: plt.savefig(save, bbox_inches="tight", dpi=300); print(f"[saved] {save}")
    if show: plt.show()
    plt.close(fig)

def _read_lambdalist_simple(lf: Path) -> dict:
    """
    Accepts either 'label wavelength' or 'wavelength' per line.
    Returns dict: label_or_index -> wavelength_nm
    """
    m = {}
    with open(lf, "r") as f:
        for i, line in enumerate(f, 1):
            s = line.strip()
            if not s or s[0] in "#%/":
                continue
            parts = s.split()
            if len(parts) == 1:
                # single column: wavelength only; index by line number
                m[str(i)] = float(parts[0])
            else:
                m[parts[0]] = float(parts[1])
    if not m:
        raise RuntimeError(f"{lf} exists but no wavelengths parsed.")
    return m

def _choose_sim_index_for_lambda(lmap: dict, target_nm: float) -> int:
    """
    From a dict like {'job.1': 532, 'job.2': 650, '3': 780, ...},
    return the integer sim index whose λ is best (exact first, else nearest).
    """
    # Build index->λ by extracting trailing integer from keys; fallback to enumerate
    idx_to_wl = {}
    for k, wl in lmap.items():
        m = re.search(r"(\d+)$", k)
        if m:
            idx_to_wl[int(m.group(1))] = wl
    if not idx_to_wl:
        # fallback: assign 1..N in file order
        idx_to_wl = {i+1: wl for i, wl in enumerate(lmap.values())}

    # exact match first
    for idx, wl in idx_to_wl.items():
        if abs(wl - target_nm) < 1e-9:
            return idx
    # nearest
    return min(idx_to_wl.keys(), key=lambda i: abs(idx_to_wl[i] - target_nm))

