def plot_fields_1d(ax, points, vec_field, which="total", func_mode="abs",
                   line_spec=None, tol=1e-6, hlines: Optional[List[float]] = None,
                   title: str = "", x_label: str = "Values", log_scale: bool = False):
    """1D line cut: select points on AXIS=VALUE and plot scalar vs. varying coord.
    - which: 'total', 'x', 'y', 'z'
    - func_mode: 'intensity', 'magnitude', 'real', 'imag', 'phase'
    - line_spec: 'x=0' | 'y=...' | 'z=...'
    - tol: geometric tolerance in nm
    - 'hlines' overlays horizontal guides (e.g., layer interfaces).
    - title: plot title
    - x_label: label for the horizontal axis (Values)
    """
    if not line_spec:
        raise ValueError("--line AXIS=VALUE is required for 1d mode")
    ax_idx, ax_val = _parse_line_spec(line_spec)

    # Select quantity and function mode
    scalar = _select_quantity(vec_field, which, mode=func_mode)

    # Build series (grid-aware). x_native is the varying spatial coord along the line.
    x_native, y_scalar, coord_label = _extract_line_series(points, scalar, ax_idx, ax_val, tol=tol)

    # Values on x-axis (optionally log10)
