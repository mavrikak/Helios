def resolve_points_path(points_arg: str, sim: str, root: Path) -> Tuple[Path, str]:
    """
    Resolve a points file from multiple user-friendly inputs:
    1) absolute/relative path,
    2) sim_res/<sim>/points/<name>,
    3) any match under sim_res/<sim>/**.
    Returns (absolute_path, points_name) where points_name tags the subfolder in fields/.
    """
    p = Path(points_arg)
    if p.exists():
        return p.resolve(), (p.parent.name if p.parent.name not in ("points", "") else p.stem)

    pd = sim_points_dir(sim, root)
    for cand in [pd / points_arg, sim_root_dir(sim, root) / points_arg]:
        if cand.exists():
            return cand.resolve(), (cand.parent.name if cand.parent != pd else cand.stem)

    matches = list(pd.rglob(points_arg))
    if matches:
        m = matches[0].resolve()
        return m, (m.parent.name if m.parent != pd else m.stem)

    raise FileNotFoundError(f"Points file not found: {points_arg} (searched {pd}/**/)")

def load_points(points_file: Path) -> np.ndarray:
    """
    Load plain XYZ points, ignoring comments and forgiving separators (, ; spaces).
    Raises if no valid triplets are found.
    """
    pts: List[List[float]] = []
    with open(points_file, "r") as f:
        for line in f:
            s = line.strip()
            if not s or s[0] in "#%/":
                continue
            s = s.replace(",", " ").replace(";", " ")
            parts = s.split()
            if len(parts) < 3: continue
            try:
                x, y, z = float(parts[0]), float(parts[1]), float(parts[2])
            except ValueError:
                continue
            pts.append([x, y, z])
    if not pts:
        raise ValueError(f"No points parsed from {points_file}")
    return np.asarray(pts, dtype=float)

