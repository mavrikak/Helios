"""
visualization.py — Plot HELIOS results (cross sections & near-fields).

This script loads outputs produced under sim_res/<sim>/ by run_sie.py and
renders either:
  (a) scattering/absorption/extinction cross sections vs. wavelength from *.csc,
  (b) near-field distributions from SIENanoPP outputs (.ein/.esc/.hin/.hsc)
      as 2D plane heat-maps or 3D colored scatter plots, and
  (c) 1D field cuts along a line within a plane (mode: 1d).

───────────────────────────────────────────────────────────────────────────────
Inputs & layout (relative to repository root)
───────────────────────────────────────────────────────────────────────────────
sim_res/<sim>/
  ├─ out/
  │   ├─ csc/                    # cross-section triplets per job
  │   └─ fields/
  │       ├─ <points_name>/      # preferred location for field files of a points set
  │       └─ sim<i>_inc_<j>.*    # fallback search if the subfolder is missing
  ├─ points/                     # .pos files with XYZ sample points (one per line)
  └─ lambdalist.txt              # optional map: job.<i> -> λ[nm] (auto-built if missing)

Points files are plain "x y z" text with no header. Field files are parsed
into complex vectors per line; flexible formats are recognized.

───────────────────────────────────────────────────────────────────────────────
Capabilities
───────────────────────────────────────────────────────────────────────────────
- Cross sections: read .csc, associate each curve with wavelength [nm] from
  lambdalist.txt or by parsing job.N.xml when needed; plot SCS/ACS/EXT.
- Near-fields: load E/H field files, choose quantity (|E|, Re(E_x), etc.),
  infer the plane (xz/xy/yz) from the point set, and render either:
  - 2D heat-map (pcolormesh) on rectilinear grids generated by make-points, or
  - 3D scatter fallback for arbitrary point clouds.
- 1D line cuts: select points along a geometric line defined by AXIS=VALUE
  (e.g., x=0) and plot scalar field vs. the remaining varying axis. The
  line must lie within the sampled plane (but the function will robustly infer
  the plotted axis even if the plane is not explicitly specified).

───────────────────────────────────────────────────────────────────────────────
CLI synopsis (selected)
───────────────────────────────────────────────────────────────────────────────
Cross sections:
  --plot-csc               Plot cross sections from *.csc
  --csc {scs,acs,ext}...   Which cross-section(s) to show
  --lambda-file FILE       Optional custom lambdalist (label λ_nm)

Near-fields:
  --points PATH|NAME            Points file or a name under sim_res/<sim>/points/
  --field {e,h}                 Field family to load
  --mode {2d,3d,1d}             Plot as 2D heat-map, 3D scatter, or 1D line cut
  --quantity {total,x,y,z}      Quantity to plot
  --function {intensity, magnitude, real, imag, phase} 
                                How to process the selected quantity
  --hlines z1 z2 ...            Horizontal guide lines (e.g., layer interfaces)

1D line cuts:
  --line AXIS=VALUE          Geometric selector for the line (e.g., x=0, y=50, z=150)
  --tol  FLOAT               Tolerance for selecting points on the line (default: 1e-6)
  Notes:
    - The line axis must belong to the detected plane (e.g., x or z for an xz plane).
    - The value is snapped to the nearest grid coordinate with a warning if far.
    - Samples are sorted by the varying in-plane coordinate before plotting.

Lambda selection:
  -L, --lambda NM          Choose the sim index closest to the requested λ[nm]

───────────────────────────────────────────────────────────────────────────────
Notes & conventions
───────────────────────────────────────────────────────────────────────────────
- Field file discovery prioritizes out/fields/<points_name>/, then falls back
  to out/fields/ and a recursive search.
- If lambdalist.txt is absent, a best-effort wavelength is inferred by parsing
  job XMLs and/or numeric stems like 'sim3'.
- 2D plots assume the points lie on a rectilinear grid (as produced by
  run_sie.py make-points). Irregular clouds are visualized with the 3D mode.
- 1D plots reuse the same grid and operate on precomputed field values only.

"""

