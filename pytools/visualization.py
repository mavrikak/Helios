def _detect_plane_axes(pts: np.ndarray) -> Tuple[str, str, int]:
    """
    Decide which 2 coordinates vary most and therefore form the plotting plane.
    Returns (axis1, axis2, fixed_index) where fixed_index is the column that is constant-ish.
    """
    uniq_counts = [len(np.unique(pts[:,i])) for i in range(3)]
    # pick two largest counts; if tie, prefer xz then xy then yz
    order = np.argsort(uniq_counts)[::-1]
    a, b, c = order[0], order[1], order[2]
    names = ["x","y","z"]
    # map to user-friendly default preference
    if set([a,b]) == set([0,2]): return ("x","z", 1)  # xz plane, fix y
    if set([a,b]) == set([0,1]): return ("x","y", 2)  # xy plane, fix z
    if set([a,b]) == set([1,2]): return ("y","z", 0)  # yz plane, fix x
    return (names[a], names[b], c)

def _grid_from_points(pts: np.ndarray, values: np.ndarray,
                      axes: Optional[str] = None) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """
    Given points (N,3) and scalar values (N,), return (X, Y, V) suitable for pcolormesh.
    If axes is provided ('xz','xy','yz'), use that; else auto-detect.
    Assumes data lie on a rectilinear grid (as in typical plane-point files).
    """
    if axes is None:
        a1, a2, fixed = _detect_plane_axes(pts)
        axes = a1 + a2
    axes = axes.lower()
    idx = {"x":0,"y":1,"z":2}
    i = idx[axes[0]]; j = idx[axes[1]]

    xs = np.unique(pts[:, i])
    ys = np.unique(pts[:, j])
    # Build map from (x,y) -> value
    xi = {v:k for k,v in enumerate(xs)}
    yj = {v:k for k,v in enumerate(ys)}
    V = np.full((len(ys), len(xs)), np.nan, dtype=float)
    for p, val in zip(pts, values):
        V[yj[p[j]], xi[p[i]]] = val

    # Create X,Y mesh (as 2D arrays) from unique vectors
    X, Y = np.meshgrid(xs, ys)
    return X, Y, V

def _plane_grid_info(points, tol=None):
    """
    Infer the plane (two varying axes), the fixed axis, and per-axis unique coords.
    Returns:
        vary_axes: tuple(int,int)       # e.g., (0,2) for xz
        fixed_axis: int                 # e.g., 1 for y (xz plane)
        coords: dict[int, np.ndarray]   # unique sorted coords for each axis
        steps: dict[int, float]         # median step for each varying axis
    Assumes a rectilinear plane grid like the one created by make-points.
    """
    pts = np.asarray(points)
    uniq = [np.unique(pts[:, i]) for i in range(3)]
    lens = [len(u) for u in uniq]
    vary_axes = tuple(sorted(np.argsort(lens)[-2:]))
    fixed_axis = ({0,1,2} - set(vary_axes)).pop()
    coords = {i: np.sort(np.unique(pts[:, i])) for i in range(3)}
    def median_step(arr):
        if len(arr) < 2: return 0.0
        dif = np.diff(arr); dif = dif[np.abs(dif) > 0]
        return float(np.median(dif)) if dif.size else 0.0
    steps = {i: median_step(coords[i]) for i in vary_axes}
    return vary_axes, fixed_axis, coords, steps


def _parse_line_spec(spec: str):
    """Parse 'x=0', 'y=50', 'z=150' -> (axis_index, value)."""
    s = spec.strip().lower().replace(" ", "")
    if "=" not in s:
        raise ValueError("line spec must be AXIS=VALUE, e.g., x=0")
    ax, val = s.split("=", 1)
    idx_map = {"x": 0, "y": 1, "z": 2}
    if ax not in idx_map:
        raise ValueError("line axis must be one of x,y,z")
    try:
        v = float(val)
    except Exception:
        raise ValueError(f"invalid numeric value in --line: {val}")
    return idx_map[ax], v


def _extract_line_series(points, scalar, line_axis, line_value, tol=1e-6):
    """
    Grid-aware 1D extraction for plane data created by make-points.
    - Verifies the line axis belongs to the plane's varying axes.
    - Snaps the requested value to the nearest grid coordinate along that axis.
    - Selects all points on that snapped line and returns (x_sorted, y_sorted, x_label).
    """
    pts = np.asarray(points); val = np.asarray(scalar)
    if pts.shape[0] != val.shape[0]:
        raise ValueError("points and scalar must have the same number of rows")

    vary_axes, fixed_axis, coords, steps = _plane_grid_info(pts)
    if line_axis not in vary_axes:
        plane = "".join("xyz"[a] for a in vary_axes)
        raise ValueError(
            f"--line must fix one of the plane axes ({plane}). "
            f"Axis '{'xyz'[line_axis]}' appears fixed ({'xyz'[fixed_axis]}-normal plane)."
        )

    grid = coords[line_axis]
    if grid.size == 0:
        raise ValueError("could not infer grid along requested axis")

    idx = int(np.argmin(np.abs(grid - line_value)))
    snapped = float(grid[idx])

    step = steps.get(line_axis, 0.0)
    if step > 0 and abs(snapped - line_value) > 0.51 * step:
        print(f"[warn] --line { 'xyz'[line_axis] }={line_value} far from grid; snapped to {snapped} (step≈{step})")

    atol = max(tol, step * 0.25 if step > 0 else tol)
    mask = np.isclose(pts[:, line_axis], snapped, atol=atol)
    if not np.any(mask):
        raise ValueError("no points found on the requested line after snapping (adjust --tol)")

    subp = pts[mask]; subv = val[mask]
    other_axis = vary_axes[1] if vary_axes[0] == line_axis else vary_axes[0]
    x = subp[:, other_axis]
    order = np.argsort(x)
    x_sorted = x[order]
    y_sorted = subv[order]
    if np.iscomplexobj(y_sorted):
        y_sorted = np.abs(y_sorted)
    x_label = "xyz"[other_axis]
    return x_sorted, y_sorted, x_label

def plot_fields(points: np.ndarray, color_vals: np.ndarray, label: str,
                title: str = "", save: Optional[Path] = None, show: bool = True):
    """
    3D heat-map of the field on the detected (or hinted) plane using pcolormesh.
    """
    fig = plt.figure(figsize=(15.5, 6.5))
    ax = fig.add_subplot(111, projection='3d')
    sc = ax.scatter(points[:,0], points[:,1], points[:,2], c=color_vals, s=12)
    cb = fig.colorbar(sc, ax=ax, shrink=0.7, pad=0.05); cb.set_label("Values")
    ax.set_xlabel("x [nm]"); ax.set_ylabel("y [nm]"); ax.set_zlabel("z [nm]")
    ax.set_title(title)
    if save:
        plt.savefig(save, bbox_inches="tight", dpi=300); print(f"[saved] {save}")
    if show: plt.show()
    plt.close(fig)

def plot_fields_2d(points: np.ndarray,
                   color_vals: np.ndarray,
                   axes_hint: Optional[str],
                   hlines: Optional[List[float]],
                   title: str = "",
                   save: Optional[Path] = None,
                   show: bool = True):
    """
    2D heat-map of the field on the detected (or hinted) plane using pcolormesh.
    - 'hlines' overlays horizontal guides (e.g., layer interfaces).
    """
    # grid in native coordinates (assumed nm)
    X, Y, V = _grid_from_points(points, color_vals, axes=axes_hint)

    fig, ax = plt.subplots(figsize=(4.6, 4.2))
    h = ax.pcolormesh(X, Y, V, shading="auto")
    cb = fig.colorbar(h, ax=ax)
    cb.set_label("Values")

    # axis labels
    _, fixed_axis, _, _ = _plane_grid_info(points)
    if fixed_axis == 0:
        ax.set_xlabel(f"y [nm]")
        ax.set_ylabel(f"z [nm]")
    elif fixed_axis == 1:
        ax.set_xlabel(f"x [nm]")
        ax.set_ylabel(f"z [nm]")
    else:
        ax.set_xlabel(f"x [nm]")
        ax.set_ylabel(f"y [nm]")
