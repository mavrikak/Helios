        # Optional log10 scaling for 2D/3D color values
        if args.log:
            # avoid log(0) and negative/complex issues
            color_vals = np.log10(np.maximum(np.abs(color_vals), 1e-30))        
        
        # Construct title based on selections
        sym = args.field.upper()
        sub = args.part
        
        if args.quantity == "total":
            # Vector notation
            base = fr"\mathbf{{{sym}}}_{{{sub}}}"
            if args.function == "intensity":
                title = fr"$|{base}|^2$"
