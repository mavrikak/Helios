def _parse_complex_triplet_line(line: str) -> Optional[List[complex]]:
    """
    Parse one line into three complex components (Ex,Ey,Ez or Hx,Hy,Hz).
    Accepts:
    - '(Re,Im) (Re,Im) (Re,Im)'     <- canonical HELIOS output
    - 'Re Im  Re Im  Re Im'         <- bare numeric pairs
    - Python-like tokens with 'i/j' <- tolerant fallback
    Returns list[complex] length 3, or None for blanks/comments.
    """
    s = line.strip()
    if not s or s[0] in "#%/":
        return None

    # 1) Fast path: explicit (Re,Im) tuples
    m_all = re.findall(r"\(\s*([-+]?[\d\.Ee+-]+)\s*,\s*([-+]?[\d\.Ee+-]+)\s*\)", s)
    if len(m_all) >= 3:
        trip = [complex(float(a), float(b)) for (a, b) in m_all[:3]]
        return trip

    # 2) Six bare numbers: Re Im Re Im Re Im
    nums = re.findall(r"[-+]?\d*\.?\d+(?:[eE][-+]?\d+)?", s)
    if len(nums) >= 6:
        a1, b1, a2, b2, a3, b3 = [float(v) for v in nums[:6]]
        return [complex(a1, b1), complex(a2, b2), complex(a3, b3)]

    # 3) Fallback: try python complex after normalizing 'i'->'j'
    s2 = s.replace("i", "j")
    toks = s2.replace(";", " ").split()
    row: List[complex] = []
    for t in toks:
        t = t.replace("*1j", "j")
        try:
            row.append(complex(t))
        except Exception:
            # also accept a lone real
            try:
                row.append(complex(float(t), 0.0))
            except Exception:
                pass
    return row[:3] if len(row) >= 3 else None

def _load_vec_field(file_path: Path, fill_policy: str = "nan") -> np.ndarray:
    """
    Read a vector field file and return an (N,3) complex array.
    Keeps row count: unparsable/placeholder lines are filled, not skipped.
    fill_policy: "nan" -> (nan+1j*nan), "big" -> (1e10+1j*1e10)
    """
    def _filler():
        if fill_policy.lower() == "big":
            v = complex(1e10, 1e10)
            return [v, v, v]
        # default: NaNs
        v = complex(np.nan, np.nan)
        return [v, v, v]

    data: List[List[complex]] = []
    with open(file_path, "r") as f:
        for raw in f:
            s = raw.strip()
            # treat an explicit "None" (or variants) as a data row that needs filling
            if s and s.lower().startswith("none"):
                data.append(_filler())
                continue

            parsed = _parse_complex_triplet_line(raw)
            if parsed is None:
                # blank/comment -> preserve alignment by filling (instead of skipping)
                data.append(_filler())
            else:
                # ensure triplet length
                if len(parsed) < 3:
                    data.append(_filler())
                else:
                    data.append(parsed[:3])

    if not data:
        raise ValueError(f"No vector data parsed from {file_path}")
    return np.asarray(data, dtype=np.complex128)

def _find_field_files(fields_dir: Path, ext: str, points_name: Optional[str],
                      sim_idx: Optional[int] = None,
                      subjob_idx: Optional[int] = None) -> List[Path]:
    """
    Return candidate field files for extension 'ext' with priority:
    1) fields/<points_name>/sim{idx}_*.ext (if points_name set),
    2) fields/sim{idx}_*.ext,
    3) fields/**/sim{idx}_*.ext (recursive fallback).
    If sim_idx is None, use '*.ext' at each step.

    If subjob_idx is provided, keep only files whose name matches:
        sim<digits>_<token>_<subjob>.<ext>   (e.g., sim1_inc_2.ein)
    """
    patt = f"*.{ext}" if sim_idx is None else f"sim{sim_idx}_*.{ext}"

    def _filter_subjob(files: List[Path]) -> List[Path]:
        if subjob_idx is None:
            return files
        kept = []
        for p in files:
            m = re.search(r"^sim\d+_[A-Za-z]+_(\d+)\." + re.escape(ext) + r"$", p.name)
            if m and int(m.group(1)) == int(subjob_idx):
                kept.append(p)
        return kept

    files: List[Path] = []
    if points_name:
        p1 = fields_dir / points_name
        if p1.exists():
            files = sorted([p for p in p1.glob(patt) if p.is_file()])
            files = _filter_subjob(files)
            if files:
                return files

    files = sorted([p for p in fields_dir.glob(patt) if p.is_file()])
    files = _filter_subjob(files)
    if files:
        return files

    files = sorted([p for p in fields_dir.rglob(patt) if p.is_file()])
    files = _filter_subjob(files)
    return files

def _load_field(fields_dir: Path, what: str, points_name: Optional[str],
                sim_idx: Optional[int] = None, subjob_idx: Optional[int] = None,
                fill_policy: str = "nan") -> np.ndarray:
    """Load a field file with extension 'what'."""
    files = _find_field_files(fields_dir, what, points_name, sim_idx=sim_idx, subjob_idx=subjob_idx)
    if not files:
        raise FileNotFoundError(f"No '.{what}' files in {fields_dir} (searched recursively)")
    arrs = [_load_vec_field(p, fill_policy=fill_policy) for p in files]
    return np.vstack(arrs)

def _unique_sorted(values: np.ndarray, tol: float = 1e-9) -> np.ndarray:
    """Unique 1D with tolerance (for z-levels)."""
    vs = np.array(values, dtype=float)
    vs.sort()
    out = []
    for v in vs:
        if not out or abs(v - out[-1]) > tol:
            out.append(v)
    return np.asarray(out, dtype=float)

def _parse_job_for_label(jobs_dir, label_str):
    """
    Resolve the propagation vector from the job XML that generated a label like:
      'out/sim<i>_inc_<j>'
    Extract <i> (job index) and <j> (subjob index). For subjob==1, read from the
    top-level <job><planewave>. For subjob>1, locate the <subjob> whose <label>
    exactly matches label_str and read its <planewave>. Polarization is ignored
    (it is provided as input elsewhere).
    Returns kvec.
    """
    m = re.search(r"sim(\d+)_inc_(\d+)", label_str)
    if not m:
        raise FileNotFoundError(f"Unrecognized label format: {label_str}")
    job_idx = int(m.group(1)); sub_idx = int(m.group(2))
    job_xml = Path(jobs_dir) / f"job.{job_idx}.xml"
    if not job_xml.exists():
        raise FileNotFoundError(f"Missing job XML: {job_xml}")
    txt = job_xml.read_text(errors="ignore")
    # pick the block to parse
    if sub_idx == 1:
        block_m = re.search(r"<\s*job\b[\s\S]*?</\s*job\s*>", txt, re.IGNORECASE)
        block = block_m.group(0) if block_m else txt
    else:
        block = None
        for sm in re.finditer(r"<\s*subjob\b[\s\S]*?</\s*subjob\s*>", txt, re.IGNORECASE):
            sblk = sm.group(0)
            lm = re.search(r"<\s*label\s*>\s*([^<]+)\s*</\s*label\s*>", sblk, re.IGNORECASE)
            if lm and lm.group(1).strip() == label_str:
                block = sblk; break
        if block is None:
            # fallback by index order if labels are absent/mismatched
            allsubs = list(re.finditer(r"<\s*subjob\b[\s\S]*?</\s*subjob\s*>", txt, re.IGNORECASE))
            sib = max(0, sub_idx - 2)
            block = allsubs[sib].group(0) if sib < len(allsubs) else txt
    # parse propagation within chosen block
    km = re.search(r"<\s*propagation[^>]*>([^<]+)<", block, re.IGNORECASE)
    if km:
        nums = [float(t) for t in re.findall(r"[-+]?\d*\.?\d+(?:[eE][-+]?\d+)?", km.group(1))]
        kvec = np.array((nums + [0, 0, 1])[:3], dtype=float)
    else:
        kvec = np.array([0.0, 0.0, 1.0], dtype=float)  # default +z
    return kvec

def _select_quantity(field, comp, mode="magnitude"):
    """
    Select quantity (total vector or x/y/z scalar) and apply operation (intensity, magnitude, real, etc).
    field: (N, 3) complex array
    """
    comp = comp.lower()
    mode = mode.lower()

    # 1. Identify raw data: Vector (total) or Scalar (component)
    if comp in ["total"]:
        raw = field
        is_vector = True
    else:
        idx = {"x": 0, "y": 1, "z": 2}[comp]
        raw = field[:, idx]
        is_vector = False

    # 2. Apply Operation
    if mode == "intensity":
        # |v|^2 for scalar, |vx|^2+|vy|^2+|vz|^2 for vector
        if is_vector:
            return np.sum(np.abs(raw)**2, axis=1)
        return np.abs(raw)**2

    elif mode == "real":
        # Only valid for scalar components. For vector, fallback to magnitude.
        if is_vector:
            return np.sqrt(np.sum(np.abs(raw)**2, axis=1))
        return np.real(raw)

    elif mode == "imag":
        if is_vector:
            return np.sqrt(np.sum(np.abs(raw)**2, axis=1))
        return np.imag(raw)

    elif mode == "phase":
        if is_vector:
            # Phase undefined for total vector magnitude; return 0 or fallback
            return np.zeros(len(raw)) 
        return np.angle(raw)

    else: 
        # "magnitude", "abs", or default
        if is_vector:
            return np.sqrt(np.sum(np.abs(raw)**2, axis=1))
        return np.abs(raw)

